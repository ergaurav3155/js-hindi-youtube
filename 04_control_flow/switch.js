// Define a variable for the month
const month = "march";

// Use a switch statement to determine the full month name
switch (month) {
  case "jan":
    console.log("January");
    break;
  case "feb":
    console.log("February");
    break;
  case "march":
    console.log("March");
    break;
  case "april":
    console.log("April");
    break;
  case "may":
    console.log("May");
    break;
  case "june":
    console.log("June");
    break;
  case "july":
    console.log("July");
    break;
  case "aug":
    console.log("August");
    break;
  case "sept":
    console.log("September");
    break;
  case "oct":
    console.log("October");
    break;
  case "nov":
    console.log("November");
    break;
  case "dec":
    console.log("December");
    break;

  default:
    console.log("Invalid month. Please enter a valid month (jan-dec).");
    break;
}

